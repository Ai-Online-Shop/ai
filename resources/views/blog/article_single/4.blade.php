    <p class="t-blog">Firstly, a Help Center with answers to typical questions like: how do I reset my account password or how do I follow up on my shipment had been set up. Afterwards, automated customer service agents have been programmed to filter the phone calls of the customers.</p>
    <p class="t-blog">From then on your customers will be redirected to either a standardized answer in the Help-Center or to an employee, who is responsible for answering the question.</p>
    <h3>Let’s do some math now:</h3>
    <p class="t-blog">Let’s assume 20% of the calls can be managed by a standardized answer from the Help-Center. This means 80% would still need human help. But due to the great filter of the AI at the beginning of the call, the time saved per call is 5 minutes on average.</p>
    <p class="t-blog"><strong>When we assume that a call takes on average 15 minutes, then per day the AI would handle 60 hours of filtering the customers and 35 hours would be answering questions with the help of the Help Center. That is a total time saving of 95 hours per day.</strong></p>
    <p class="t-blog">Let’s further assume that an average employee can handle 7 hours of phone calls per day. Therefore, the results of the AI could lead to a reduction of around 13 employees</p>
    <p class="t-blog">Moreover, let’s assume one employee received around $2,500 per month as a salary. With additional costs like insurances and vocation money, the total costs per employee add up to $3,500 per month.</p>
    <p class="t-blog quote-gr-blue"><strong>The AI could save the company $45,500 per month and $546,000 per year in personnel costs.</strong></p>
    <p class="t-blog">With this example in mind, you could understand why the investments for automated customer service agents reach $4.5 billion worldwide this year.<sup class="source-no" role="button" tabindex="0" on="tap:6.scrollTo(duration=500, position=top)">[1]</sup></p>
